CREATE DATABASE wpproj4;
USE wpproj4;
CREATE TABLE seller_data (
    seller_id INT AUTO_INCREMENT PRIMARY KEY,
    seller_name VARCHAR(255) NOT NULL,
    seller_user_id VARCHAR(255) UNIQUE NOT NULL,
    seller_email VARCHAR(255) UNIQUE NOT NULL,
    seller_password VARCHAR(255) NOT NULL
);

CREATE TABLE buyer_data (
    buyer_id INT AUTO_INCREMENT PRIMARY KEY,
    buyer_name VARCHAR(255) NOT NULL,
    buyer_user_id VARCHAR(255) UNIQUE NOT NULL,
    buyer_email VARCHAR(255) UNIQUE NOT NULL,
    buyer_password VARCHAR(255) NOT NULL
);